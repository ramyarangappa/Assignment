package Module7;

public class SortingComparison {
        public static void main(String[] args) {

            System.out.println("Sorting Algorithm Complexity Comparison\n");

            System.out.println("Bubble Sort:");
            System.out.println("Best Time: O(n)");
            System.out.println("Average Time: O(n^2)");
            System.out.println("Worst Time: O(n^2)");
            System.out.println("Space: O(1)\n");

            System.out.println("Selection Sort:");
            System.out.println("Best Time: O(n^2)");
            System.out.println("Average Time: O(n^2)");
            System.out.println("Worst Time: O(n^2)");
            System.out.println("Space: O(1)\n");

            System.out.println("Insertion Sort:");
            System.out.println("Best Time: O(n)");
            System.out.println("Average Time: O(n^2)");
            System.out.println("Worst Time: O(n^2)");
            System.out.println("Space: O(1)\n");

            System.out.println("Merge Sort:");
            System.out.println("Best Time: O(n log n)");
            System.out.println("Average Time: O(n log n)");
            System.out.println("Worst Time: O(n log n)");
            System.out.println("Space: O(n)\n");

            System.out.println("Quick Sort:");
            System.out.println("Best Time: O(n log n)");
            System.out.println("Average Time: O(n log n)");
            System.out.println("Worst Time: O(n^2)");
            System.out.println("Space: O(log n)");
        }
    }

